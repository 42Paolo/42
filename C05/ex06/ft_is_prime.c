#include <unistd.h>
#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	n;

	n = 1;
	if (nb < 0 || nb == 0 || nb == 1)
		return (0);
	while (n <= nb)
	{
		if (nb % n == 0 && n != 1 && n != nb)
			return (0);
		n++;
	}
	return (1);
}
