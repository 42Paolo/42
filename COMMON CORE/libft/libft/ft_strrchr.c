#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*str = s;

	while (*s)
		s++;
	if ((char)c == '\0')
		return ((char *)s);
	while (s >= str)
	{
		if (*s == (char)c)
			return ((char *)s);
		s--;
	}
	return (NULL);
}